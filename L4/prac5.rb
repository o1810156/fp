def perm(a, n, s="")
	if n == 0
		return s
	end

	res = []
	a.each_index do |i|
		nxt = a.dup
		nxt.delete_at(i)
		res.push(*perm(nxt, n-1, s+a[i].to_s))
	end
	return res
end

r = perm([1, 2, 3, 4, 5], 3)

for _p in r
	p _p
end

p r.length

